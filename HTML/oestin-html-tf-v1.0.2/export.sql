  CREATE OR REPLACE NONEDITIONABLE FUNCTION "JAYBNB"."FN_DEVUELVE_NUMERO" (f_desde VARCHAR , f_hasta VARCHAR)
    return number 
    is
    
    fecha_no_disponible exception;
    c_prop_disponibles SYS_REFCURSOR;
    cantidad number:=0;
    aux number:=0;
begin  

    begin

        select count(p4.id_propiedad) into cantidad
        from propiedad p4
        where p4.id_propiedad NOT IN
        (
            SELECT p.ID_PROPIEDAD
            FROM propiedad p
            LEFT JOIN reserva r
            ON r.id_propiedad = p.ID_PROPIEDAD
            WHERE r.ID_RESERVA IS  NULL
            AND ( 
            (TO_DATE(f_desde, 'YYYY-MM-DD') > r.FECHA_TERMINO_RESER)
            OR
            (TO_DATE(f_desde, 'YYYY-MM-DD') < r.FECHA_INICIO_RESER
            AND
            TO_DATE(f_hasta, 'YYYY-MM-DD') < r.FECHA_INICIO_RESER )
            )
            OR
            (r.FECHA_INICIO_RESER BETWEEN TO_DATE(f_desde, 'YYYY-MM-DD') AND TO_DATE(f_hasta, 'YYYY-MM-DD'))
            OR 
            r.FECHA_TERMINO_RESER BETWEEN TO_DATE(f_desde, 'YYYY-MM-DD') AND TO_DATE(f_hasta, 'YYYY-MM-DD')
            )
         ORDER BY ID_PROPIEDAD;     

    end; 
       return cantidad;
       DBMS_OUTPUT.PUT_LINE(cantidad);      

end FN_devuelve_numero;

