procedure sp_crear_reserva (
P_FECHA_INICIO_RESER  VARCHAR2,
P_FECHA_TERMINO_RESER  VARCHAR2,
P_CANTIDAD_ACOMPA  RESERVA.CANTIDAD_ACOMPA%TYPE,
P_ID_PROPIEDAD  RESERVA.ID_PROPIEDAD%TYPE,
P_ID_CLIENTE  RESERVA.ID_CLIENTE%TYPE
)
as
        v_dias number:=0;
        v_monto_total number:=0;
        v_id_estado_prop varchar2(50):='';
        v_result varchar2(60):='';
        v_nom_estado_prop varchar2(30):='';
        v_meses number:=0;                
        v_cumple_fecha number:=0;
        v_cump_ocupantes number:=0;

begin
        /*Bloque anonimo para validar cumplimiento de fechas en el futuro*/                  
        begin
                if TO_DATE(P_FECHA_TERMINO_RESER, 'YYYY-MM-DD') > TO_DATE(P_FECHA_INICIO_RESER, 'YYYY-MM-DD')  then   
                    SELECT round(MONTHS_BETWEEN(TO_DATE(P_FECHA_TERMINO_RESER, 'YYYY-MM-DD'),TO_DATE(P_FECHA_INICIO_RESER, 'YYYY-MM-DD'))) Meses into v_meses
                    FROM DUAL;                    
                        if v_meses<=6 then
                                v_cumple_fecha:=1;
                        else
                                dbms_output.put_line('Solo puede reservar hasta 6 meses en el futuro'||' intento con: '||v_meses||' meses'); 
                                v_cumple_fecha:=-1;
            
                        end if;
                else
                    dbms_output.put_line('Fecha termino no puede ser menor a fecha actual'); 
                    v_cumple_fecha:=-1;
            
                end if;
        end;
        /*******************************************************************************/
        
        /************Bloque para validar cumplimiento de ocupantes********************************/
        begin
            select canti_max_ocup into v_cump_ocupantes
            from propiedad
            where id_propiedad=P_ID_PROPIEDAD;
            v_monto_total:= v_monto_total*v_dias;
                if P_CANTIDAD_ACOMPA <=v_cump_ocupantes then
                     v_cump_ocupantes:=1;
                     dbms_output.put_line('Ocupantes cumple con la cantidad máxima permitida'); 
                else
                     v_cump_ocupantes:=-1;
                    dbms_output.put_line('Ocupantes supera la cantidad máxima permitida');  

                end if;
        end;        
            
         /*****************************************************************************************/
        
        
        /*bloque para el calculo de la cantidad de dias*/
        begin
            select trunc(TO_DATE(P_FECHA_TERMINO_RESER, 'YYYY-MM-DD'))-trunc(TO_DATE(P_FECHA_INICIO_RESER, 'YYYY-MM-DD')) into v_dias
            From dual;
            dbms_output.put_line(v_dias ||' días de reserva');  
        end;
        /*********************************************************************************************************/
        /*Realizamos el calculo del total a pagar por las noches*/ 
        begin
            select valor_noche into v_monto_total
            from propiedad
            where id_propiedad=1;
            v_monto_total:= v_monto_total*v_dias;
            dbms_output.put_line('A pagar: '||v_monto_total);  
        end;   
    /*Vamos a preguntar si la propiedad esta disponible para reservarse*/
    
    begin
        select p.id_estado_propi, ep.nombre_estado into v_id_estado_prop, v_nom_estado_prop
        from propiedad p  
        join estado_propi ep on p.id_estado_propi=ep.id_estado_propi
        where p.id_propiedad = P_ID_PROPIEDAD;        
        case 
           when v_id_estado_prop = 1 and v_cumple_fecha=1 and v_cump_ocupantes=1 then   
           v_result:='Propiedad disponible, cumple ocupantes y fecha valida';           
            dbms_output.put_line(v_result);  
            dbms_output.put_line('insertando reserva!!!'); 
                declare
                    v_fecha_reserva date;
                    v_id_estado_reserva number;
                begin
                    v_fecha_reserva:=sysdate;
                    v_id_estado_reserva:=1;
                        Insert into reserva (
                            ID_RESERVA
                            ,FECHA_RESERVA
                            ,FECHA_INICIO_RESER
                            ,FECHA_TERMINO_RESER
                            ,CANTIDAD_ACOMPA
                            ,NUM_NOCHE
                            ,MONTO_TOTAL
                            ,ID_PROPIEDAD
                            ,ID_CLIENTE
                            ,ID_ESTADO_RESE                
                        )values(
                             S_RESERVA.nextval
                            ,v_fecha_reserva
                            ,TO_DATE(P_FECHA_INICIO_RESER, 'YYYY-MM-DD')
                            ,TO_DATE(P_FECHA_TERMINO_RESER, 'YYYY-MM-DD')
                            ,P_CANTIDAD_ACOMPA
                            ,v_dias
                            ,v_monto_total
                            ,P_ID_PROPIEDAD
                            ,P_ID_CLIENTE
                            ,v_id_estado_reserva
                        );
						commit;
                    dbms_output.put_line('Reserva creada con éxito'); 

                    end;                   
            when v_id_estado_prop <> 1 then
                v_result:='Propiedad no esta disponible';
                dbms_output.put_line(v_result||' - '||v_nom_estado_prop||', No se realizo la reserva!!!');  
            else 
             dbms_output.put_line('Hubo otro problema, No se realizo la reserva!!!');  
        end case;     
    end;
    exception
        when no_data_found then
        dbms_output.put_line('Propiedad o cliente no encontrado');     
        when others then 
        dbms_output.put_line('Ocurrio un error!!!'); 
end;
